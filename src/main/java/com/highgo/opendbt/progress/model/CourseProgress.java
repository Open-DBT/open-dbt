package com.highgo.opendbt.progress.model;

public class CourseProgress {

	private int correct;
	private int count;

	public int getCorrect() {
		return correct;
	}

	public void setCorrect(int correct) {
		this.correct = correct;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
